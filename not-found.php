							<p>Die ehemalige Handschriftenarchiv-Sammlung des Dresdner Kreuzchores bestand schon seit vielen Jahren, wurde jedoch erst 2013 erstmals offiziell als Archiv erwähnt. Hier werden die Noten aus 100 Jahren Dresdner Kreuzchor gesammelt, sortiert und archiviert. Das Archiv hat direkten Anschluss zur Notenbibliothek, sodass immer noch der Bezug zwischen alten und neuen Noten besteht. Im Archiv selbst finden sich Noten mit einem Alter bis zu 100 Jahren und Intoriten aus der Mauersberger-Zeit. Noten die vor 1945 geschrieben wurden sind Raritäten, denn größtenteils wurden die Notenbestände im Krieg vernichtet oder sind verschwunden. So sind nur noch einige wenige Exemplare erhalten geblieben. Seit zwei Jahren werden nun die Noten aus dem alten Sammlung in ein neues Archiv umsortiert und währenddessen gleich systematisch archiviert, erfasst und digitalisiert. Das ist neu, denn vor 2 Jahren noch wurden die Noten und Handschriften noch per Hand erfasst. Doch seit das Handschriftenarchiv auch im Netz vertreten ist, bietet es sich an nebenher Webpräsens die Handschriftlichen Noten in ein "neues" Zeitalter zu überführen - sie zu digitalisieren.</p>
